public class InfiniteWhileLoop
{
    public static void main(String[] args)
    {
        int evensSum = 0;
        int nextNum = 0;
        while(nextNo <= 100);
        { 
            evensSum = evensSum + nextNum; 
            nextNum += 2; // add two to nextNo
        } // Assertion: nextNo > 100 
        System.out.println(evensSum);
    }
}